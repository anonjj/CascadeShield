def run_command(args, cwd=None):
    """Helper to run system commands."""
    result = subprocess.run(args, cwd=cwd, stdout=subprocess.PIPE, stderr=subprocess.PIPE, text=True)
    if result.returncode != 0:
        print(f"Command failed: {' '.join(args)}\nError: {result.stderr}", file=sys.stderr)
    return result

def write_env_file(config):
    """Writes the current R4J config parameters to the infra/.env file."""
    env_content = f"""# Auto-generated by CascadeShield Experiment Runner
CB_SLIDING_WINDOW_SIZE={config['slidingWindowSize']}
CB_SLIDING_WINDOW_TYPE={config['slidingWindowType']}
CB_FAILURE_RATE_THRESHOLD={config['failureRateThreshold']}
CB_WAIT_DURATION_OPEN={config['waitDurationInOpenState']}s
CB_PERMITTED_CALLS_HALF_OPEN={config.get('permittedCallsInHalfOpenState', 5)}
"""
    os.makedirs(os.path.dirname(ENV_PATH), exist_ok=True)
    with open(ENV_PATH, "w") as f:
        f.write(env_content)
    print(f"Updated {ENV_PATH.name} with: {config}")

def update_containers():
    """Forces docker compose to recreate services with the new environment variables."""
    print("Recreating Spring Boot containers to apply new configuration...")
    # Recreate only the services using Resilience4j to save time
    services = ["gateway-service", "order-service", "inventory-service", "payment-service", "notification-service", "shared-db-service"]
    cmd = ["docker", "compose", "-f", str(COMPOSE_FILE_PATH), "up", "-d", "--no-deps", "--force-recreate"] + services
    result = run_command(cmd)
    if result.returncode != 0:
        print("Docker compose failed to recreate containers. Aborting run.", file=sys.stderr)
        return False
    return True

def wait_for_healthy(timeout=60):
    """Polls the Gateway actuator endpoint to verify the mesh is online and healthy."""
    url = "http://localhost:8080/actuator/health"
    print(f"Waiting for Gateway Service to become healthy at {url}...")
    start_time = time.time()
    while time.time() - start_time < timeout:
        try:
            with urllib.request.urlopen(url, timeout=2) as response:
                if response.status == 200:
                    data = json.loads(response.read().decode("utf-8"))
                    if data.get("status") == "UP":
                        print("Gateway is healthy!")
                        return True
        except Exception:
            pass
        time.sleep(2)
    print("Timeout waiting for Gateway health check.", file=sys.stderr)
    return False

def inject_fault(fault_type):
    """Injects the appropriate fault profile into Toxiproxy."""
    toxiproxy.reset_all()
    
    if fault_type == "latency":
        # Inject high latency (3000ms) on inventory-service-proxy
        # This simulates a slow inventory downstream dependency
        toxiproxy.inject_latency("inventory-service-proxy", delay_ms=3000, toxicity=1.0)
        
    elif fault_type == "crash":
        # Disable payment-service-proxy completely (simulates a crashed instance)
        toxiproxy.set_enabled("payment-service-proxy", False)
        
    elif fault_type == "throttle":
        # Limit database proxy bandwidth to 1 KB/s (simulates DB connection/resource limits)
        toxiproxy.inject_bandwidth_limit("shared-db-service-proxy", rate_kbps=1, toxicity=1.0)
        
    else:
        print(f"No fault injected. Type '{fault_type}' is unknown.", file=sys.stderr)

def generate_load(endpoint_url, requests_count=50, concurrency=5):
    """Lightweight built-in HTTP load generator to test the mesh."""
    print(f"Generating load: sending {requests_count} concurrent requests to {endpoint_url}...")
    
    success_count = 0
    failure_count = 0
    latencies = []
    lock = threading.Lock()
    
    def send_request():
        nonlocal success_count, failure_count
        start = time.time()
        success = False
        try:
            req = urllib.request.Request(endpoint_url)
            with urllib.request.urlopen(req, timeout=5) as res:
                res.read()
                if res.status == 200:
                    success = True
        except Exception:
            pass
            
        latency = (time.time() - start) * 1000
        with lock:
            if success:
                success_count += 1
            else:
                failure_count += 1
            latencies.append(latency)

    # Measure TPS from first-submit to last-completion only.
    # The pacing sleep (0.05s between submits) inflates total_time if we naively
    # wrap the submission loop — we instead derive elapsed from individual latencies,
    # which each start/stop on their own wall-clock inside send_request().
    # Total execution window = max individual completion time from a shared t0.
    t0 = time.time()
    with ThreadPoolExecutor(max_workers=concurrency) as executor:
        for _ in range(requests_count):
            executor.submit(send_request)
            time.sleep(0.05)  # Pacing: avoids thundering-herd on the gateway
    # executor.__exit__ blocks until ALL futures complete — t1 is post-completion.
    t1 = time.time()

    # Execution window = time from first dispatch to last completion.
    # Pacing is ~2.5s of sleep; requests run concurrently so actual work
    # overlaps heavily. We subtract the known pacing overhead to report
    # the true request-execution window.
    pacing_overhead = (requests_count - 1) * 0.05
    execution_time = max((t1 - t0) - pacing_overhead, 0.001)

    avg_latency = sum(latencies) / len(latencies) if latencies else 0
    throughput = requests_count / execution_time
    error_rate = (failure_count / requests_count) * 100 if requests_count else 0
    
    print(f"Load Results - Successes: {success_count}, Failures: {failure_count}, Avg Latency: {avg_latency:.2f}ms, Throughput: {throughput:.2f} TPS, Error Rate: {error_rate:.2f}%")
    return throughput, error_rate, avg_latency

def get_blast_radius():
    """Queries the Gateway's custom aggregator endpoint for the current blast radius.
    
    Returns the blast radius as a float, or None if the measurement failed.
    Returning None (rather than 0.0) ensures failed measurements are distinguishable
    from a genuinely healthy mesh with no open circuit breakers in the CSV dataset.
    """
    url = "http://localhost:8080/api/v1/blast-radius"
    try:
        with urllib.request.urlopen(url, timeout=2) as response:
            if response.status == 200:
                data = json.loads(response.read().decode("utf-8"))
                return data.get("blastRadius", 0.0)
    except Exception as e:
        print(f"Failed to query blast radius from Gateway: {e}", file=sys.stderr)
    return None  # Sentinel: distinguishable from a real 0.0 (healthy mesh)

def log_results(config, fault_type, mode, topology, metrics):
    """Appends experiment run results to master_dataset.csv."""
    os.makedirs(os.path.dirname(DATASET_PATH), exist_ok=True)
    file_exists = os.path.exists(DATASET_PATH)
    
    with open(DATASET_PATH, mode="a", newline="") as f:
        writer = csv.writer(f)
        if not file_exists:
            writer.writerow(DATASET_HEADERS)
            
        writer.writerow([
            time.strftime("%Y-%m-%d %H:%M:%S"),
            mode,
            topology,
            fault_type,
            config["failureRateThreshold"],
            config["slidingWindowSize"],
            config["slidingWindowType"],
            config["waitDurationInOpenState"],
            config.get("permittedCallsInHalfOpenState", 5),
            metrics["blast_radius"],
            f"{metrics['throughput']:.2f}",
            f"{metrics['error_rate']:.2f}",
            f"{metrics['avg_latency_ms']:.2f}"
        ])
    print(f"Saved run metrics to {DATASET_PATH}")

def run_experiment_run(config, fault_type, mode, topology="linear"):
    """Orchestrates a single configuration and fault run."""
    print("\n" + "="*60)
    print(f"STARTING EXPERIMENT: Mode={mode}, Topology={topology}, Fault={fault_type}")
    print(f"Config: {config}")
    print("="*60)
    
    # 1. Update environments
    write_env_file(config)
    if not update_containers():
        print("Skipping run due to Docker compose failure.", file=sys.stderr)
        return False

    # 2. Verify containers started up
    if not wait_for_healthy():
        print("Skipping run due to unhealthy services.", file=sys.stderr)
        return False
        
    # 3. Inject fault into Toxiproxy
    inject_fault(fault_type)
    
    # 4. Generate load
    endpoint = f"http://localhost:8080/api/v1/{topology}"
    throughput, error_rate, avg_latency = generate_load(endpoint)
    
    # 5. Measure blast radius
    blast_radius = get_blast_radius()
    
    # 6. Reset Toxiproxy
    toxiproxy.reset_all()
    
    # 7. Save results
    metrics = {
        "blast_radius": blast_radius,
        "throughput": throughput,
        "error_rate": error_rate,
        "avg_latency_ms": avg_latency
    }
    log_results(config, fault_type, mode, topology, metrics)
    return True

def generate_combinations(mode):
    """Generates configuration combinations depending on the mode."""
    configs = []
    
    if mode == "canary":
        # Small sample of extreme and mid configs with permittedCallsInHalfOpenState mapped
        configs = [
            # Extreme Aggressive
            {"failureRateThreshold": 30, "slidingWindowSize": 5, "waitDurationInOpenState": 5, "slidingWindowType": "COUNT_BASED", "permittedCallsInHalfOpenState": 3},
            {"failureRateThreshold": 30, "slidingWindowSize": 5, "waitDurationInOpenState": 5, "slidingWindowType": "TIME_BASED", "permittedCallsInHalfOpenState": 3},
            # Extreme Conservative
            {"failureRateThreshold": 70, "slidingWindowSize": 20, "waitDurationInOpenState": 30, "slidingWindowType": "COUNT_BASED", "permittedCallsInHalfOpenState": 10},
            {"failureRateThreshold": 70, "slidingWindowSize": 20, "waitDurationInOpenState": 30, "slidingWindowType": "TIME_BASED", "permittedCallsInHalfOpenState": 10},
            # Midpoint config
            {"failureRateThreshold": 50, "slidingWindowSize": 10, "waitDurationInOpenState": 15, "slidingWindowType": "COUNT_BASED", "permittedCallsInHalfOpenState": 5}
        ]
    else:
        # Full Sweep (3 * 3 * 3 * 2 * 3 = 162 configs per fault, 486 runs total across 3 faults)
        for threshold in PARAM_VALUES["failureRateThreshold"]:
            for window in PARAM_VALUES["slidingWindowSize"]:
                for wait in PARAM_VALUES["waitDurationInOpenState"]:
                    for wtype in PARAM_VALUES["slidingWindowType"]:
                        for permitted in PARAM_VALUES["permittedCallsInHalfOpenState"]:
                            configs.append({
                                "failureRateThreshold": threshold,
                                "slidingWindowSize": window,
                                "waitDurationInOpenState": wait,
                                "slidingWindowType": wtype,
                                "permittedCallsInHalfOpenState": permitted
                            })
    return configs

def main():
    parser = argparse.ArgumentParser(description="CascadeShield Parameter Sweep Automation Runner")
    parser.add_argument("--mode", choices=["canary", "full"], default="canary", help="canary (5 runs) or full (162 runs)")
    parser.add_argument("--fault", choices=["latency", "crash", "throttle"], default="latency", help="Fault type to inject")
    parser.add_argument("--topology", choices=["linear", "fanout", "mesh"], default="linear", help="Service mesh topology pattern")
    
    args = parser.parse_args()
    
    print(f"Starting CascadeShield Runner in '{args.mode}' mode targeting '{args.topology}' topology with '{args.fault}' fault.")
    
    # Verify Toxiproxy is reachable
    try:
        toxiproxy.setup_default_proxies()
        toxiproxy.reset_all()
    except Exception:
        print("Toxiproxy is not running or unreachable on http://localhost:8474. Make sure Docker is running.", file=sys.stderr)
        sys.exit(1)
        
    configs = generate_combinations(args.mode)
    print(f"Generated {len(configs)} configuration sweep profiles.")
    
    success_runs = 0
    for i, config in enumerate(configs):
        print(f"\nProgress: Run {i+1} of {len(configs)}")
        success = run_experiment_run(config, args.fault, args.mode, args.topology)
        if success:
            success_runs += 1
            
    print("\n" + "="*60)
    print(f"SWEEP COMPLETED: {success_runs}/{len(configs)} runs executed successfully.")
    print(f"Master dataset: {DATASET_PATH}")
    print("="*60)

if __name__ == "__main__":
    main()
